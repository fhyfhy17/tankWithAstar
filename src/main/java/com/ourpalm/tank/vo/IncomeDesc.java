package com.ourpalm.tank.vo;

public class IncomeDesc {
	private int exp;
	private int roleExp;
	private int iron;
	private int honor;

	public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public int getRoleExp() {
		return roleExp;
	}

	public void setRoleExp(int roleExp) {
		this.roleExp = roleExp;
	}

	public int getIron() {
		return iron;
	}

	public void setIron(int iron) {
		this.iron = iron;
	}

	public int getHonor() {
		return honor;
	}

	public void setHonor(int honor) {
		this.honor = honor;
	}

}
