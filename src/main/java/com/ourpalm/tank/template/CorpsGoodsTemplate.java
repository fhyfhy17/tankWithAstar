package com.ourpalm.tank.template;

public class CorpsGoodsTemplate {

	private int group;
	private int id;
	private int goodsId;
	private int count;
	private int corpsGold;
	private int weight;
	
	
	public int getGroup() {
		return group;
	}
	public void setGroup(int group) {
		this.group = group;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getCorpsGold() {
		return corpsGold;
	}
	public void setCorpsGold(int corpsGold) {
		this.corpsGold = corpsGold;
	}
}
