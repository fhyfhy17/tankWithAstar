package com.ourpalm.tank.template;

public class GoodsWarTemplate {

	private int id;
	private int addBuff;
	private String delBuff;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAddBuff() {
		return addBuff;
	}
	public void setAddBuff(int addBuff) {
		this.addBuff = addBuff;
	}
	public String getDelBuff() {
		return delBuff;
	}
	public void setDelBuff(String delBuff) {
		this.delBuff = delBuff;
	}
}
